function isEven(num) {
  if (num % 2 == 0) {
    console.log(`number is even${num}`)
  } else {
    console.log(`number is not even${num}`)
  }
}

isEven(10)
isEven(11)

function isOdd(num) {
  if (num % 2 == 0) {
    console.log(`number is not odd${num}`)
  } else {
    console.log(`number is odd${num}`)
  }
}

isOdd(12)
isOdd(11)
